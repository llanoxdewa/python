# operasi aritmatika

a = 12
b = 5

# operasi tambah +
hasil = a + b
print(a ,'+', b ,'=', hasil)

# operasi pengurangan -
hasil = a - b
print(a ,'-', b ,'=', hasil)

# operasi perkalian *
hasil = a * b
print(a ,'X', b ,'=', hasil)

# operasi pembagian /
hasil = a / b
print(a ,'/', b ,'=', hasil)

# operasi eksponen (pangkat) **
hasil = a ** b
print(a ,'**', b ,'=', hasil)

# operasi modulus %
hasil = a % b
print(a ,'%', b ,'=', hasil)

# operasi floor division //
hasil = a // b
print(a ,'//', b ,'=', hasil)

# prioritas operasi
x = 3
z = 2
y = 4
hasil = x ** y * z + x / y - y % z // x
print(x,'**',y,'*',z,'+',x,'/',y,'-',y,'%',z,'//',x, '=', hasil)

